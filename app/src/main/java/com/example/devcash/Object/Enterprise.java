package com.example.devcash.Object;

public class Enterprise {
    String ent_addr, ent_cat, ent_name, ent_permitno, ent_telno, ent_type;
    Long ent_no_emp;


    public Enterprise() {
    }

    public String getEnt_addr() {
        return ent_addr;
    }

    public void setEnt_addr(String ent_addr) {
        this.ent_addr = ent_addr;
    }

    public String getEnt_cat() {
        return ent_cat;
    }

    public void setEnt_cat(String ent_cat) {
        this.ent_cat = ent_cat;
    }

    public String getEnt_name() {
        return ent_name;
    }

    public void setEnt_name(String ent_name) {
        this.ent_name = ent_name;
    }

    public String getEnt_permitno() {
        return ent_permitno;
    }

    public void setEnt_permitno(String ent_permitno) {
        this.ent_permitno = ent_permitno;
    }

    public String getEnt_telno() {
        return ent_telno;
    }

    public void setEnt_telno(String ent_telno) {
        this.ent_telno = ent_telno;
    }

    public String getEnt_type() {
        return ent_type;
    }

    public void setEnt_type(String ent_type) {
        this.ent_type = ent_type;
    }

    public Long getEnt_no_emp() {
        return ent_no_emp;
    }

    public void setEnt_no_emp(Long ent_no_emp) {
        this.ent_no_emp = ent_no_emp;
    }
}
